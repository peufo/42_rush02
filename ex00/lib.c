#include "lib.h"

void	put_str(char *str)
{
	while (*str)
		write(1, str++, 1);
}

int	is_space(char c)
{
	return (c == '\t'
		|| c == '\n'
		|| c == '\v'
		|| c == '\f'
		|| c == '\r'
		|| c == ' ');
}
